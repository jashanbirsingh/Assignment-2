a = 56
b = 10

# Calculating a&b (bitwise AND operation)
print(a & b)
# Calculating a|b (bitwise OR operation)
print(a | b)
# Calculating a^b (bitwise XOR operation)
print(a^b)
# Left shift both a and b with 2 bits.
print(a<<2,b<<2)
# Right shift a with 2 bits and b with 4 bits.
print(a>>2,b>>4)

